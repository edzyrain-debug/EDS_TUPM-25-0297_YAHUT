import os
import numpy as np
import pandas as pd
import matplotlib.pyplot as plt
import matplotlib.animation as animation
import seaborn as sns

class RoboticsDataPipeline:
    def __init__(self, data_dir="data", output_dir="outputs"):
        """Initializes workspace directories and system file paths."""
        self.data_dir = data_dir
        self.output_dir = output_dir
        self.raw_path = os.path.join(data_dir, "dataset_original.csv")
        self.clean_path = os.path.join(data_dir, "dataset_cleaned.csv")
        self.df = None
        
        os.makedirs(self.data_dir, exist_ok=True)
        os.makedirs(self.output_dir, exist_ok=True)

    def ingest_data(self):
        """Step 1: Loads telemetry log file with automated testing fallback."""
        print("[1/5] Ingesting robotics telemetry dataset...")
        try:
            if not os.path.exists(self.raw_path):
                print(f" -> '{self.raw_path}' absent. Generating mock haptic telemetry data...")
                self._generate_mock_csv()
            
            self.df = pd.read_csv(self.raw_path)
            print(f" -> Ingested {len(self.df)} telemetry frames successfully.")
        except Exception as e:
            print(f" [CRITICAL ERROR] Data ingestion sequence halted: {e}")
            raise

    def clean_and_filter_data(self, target_robot_id="ROBOT_MX_01"):
        """Step 2: Cleans structural errors and executes Mandatory Unique Filter Logic."""
        print("[2/5] Deploying data cleaning routines and unique filters...")
        try:
            if self.df is None:
                raise ValueError("DataFrame is empty. Execute ingest_data first.")
            
            # Remove duplicate tracking entries
            initial_count = len(self.df)
            self.df.drop_duplicates(inplace=True)
            print(f" -> Purged {initial_count - len(self.df)} redundant duplicate packets.")

            # Correct null and corrupted values
            null_count = self.df.isnull().sum().sum()
            if null_count > 0:
                print(f" -> Found {null_count} null elements. Executing linear interpolation...")
                self.df.interpolate(method='linear', inplace=True)
                self.df.dropna(inplace=True)

            # Enforce data type integrity
            self.df['Commanded_Force_N'] = pd.to_numeric(self.df['Commanded_Force_N'], errors='coerce')
            self.df['Measured_Force_N'] = pd.to_numeric(self.df['Measured_Force_N'], errors='coerce')

            # MANDATORY UNIQUE FILTER: Isolating data to a single distinct physical robot ID
            print(f" -> Applying Unique Programmatic Filter: [Robot_ID == '{target_robot_id}']")
            self.df = self.df[self.df['Robot_ID'] == target_robot_id]
            print(f" -> Unique data slice size: {len(self.df)} unique frames.")

            self.df.to_csv(self.clean_path, index=False)
            print(f" -> Cleaned pipeline data exported to: {self.clean_path}")

        except Exception as e:
            print(f" [ERROR] Data processing pipeline failed: {e}")

    def analyze_data(self):
        """Step 3: Conducts high-level engineering analytics satisfying all Section VI criteria."""
        print("[3/5] Performing Engineering Data Analytics...")
        try:
            if self.df is None or self.df.empty:
                print(" -> [WARN] Cleaned dataframe empty. Skipping analytics.")
                return {}

            # Calculate the primary engineering variable: Force Error
            # Error = Measured Force - Commanded Target Force
            self.df['Force_Error_N'] = self.df['Measured_Force_N'] - self.df['Commanded_Force_N']
            error_array = self.df['Force_Error_N'].to_numpy()
            cmd_array = self.df['Commanded_Force_N'].to_numpy()
            meas_array = self.df['Measured_Force_N'].to_numpy()

            metrics = {}

            print("\n==================================================")
            print("         ENGINEERING ANALYTICS REPORT             ")
            print("==================================================")

            # 1. DESCRIPTIVE STATISTICS (Mandatory Metrics)
            metrics["mean"] = np.mean(error_array)
            metrics["median"] = np.median(error_array)
            metrics["std_dev"] = np.std(error_array)
            metrics["variance"] = np.var(error_array)
            
            print(f"\n[1] DESCRIPTIVE STATISTICS (Force Error Deviation):")
            print(f" -> Mean Systemic Error:    {metrics['mean']:.4f} N")
            print(f" -> Median Systemic Error:  {metrics['median']:.4f} N")
            print(f" -> Standard Deviation:     {metrics['std_dev']:.4f} N")
            print(f" -> Variance (ROB-05 Key):  {metrics['variance']:.4f} N²")

            # 2. DISTRIBUTION ANALYSIS (Spread, Skewness, Outliers)
            # Mathematical Skewness primitive formula: E[(X-mu)^3] / std_dev^3
            mean_diff = error_array - metrics["mean"]
            skewness = np.mean(mean_diff**3) / (metrics["std_dev"]**3)
            metrics["skewness"] = skewness

            # Outlier boundaries using Interquartile Range (IQR)
            q75, q25 = np.percentile(error_array, [75, 25])
            iqr = q75 - q25
            metrics["outlier_upper"] = q75 + (1.5 * iqr)
            metrics["outlier_lower"] = q25 - (1.5 * iqr)
            
            # Count elements sitting outside of the normal distribution threshold
            outliers_count = np.sum((error_array > metrics["outlier_upper"]) | (error_array < metrics["outlier_lower"]))
            metrics["outlier_count"] = outliers_count

            print(f"\n[2] DISTRIBUTION ANALYSIS:")
            print(f" -> Data Spread (IQR Range): {iqr:.4f} N")
            print(f" -> Calculated Skewness:     {metrics['skewness']:.4f}")
            print(f" -> Outlier Bounds:         [{metrics['outlier_lower']:.4f} N to {metrics['outlier_upper']:.4f} N]")
            print(f" -> Total Outliers Detected: {metrics['outlier_count']} anomalous frame(s)")

            # 3. CORRELATION ANALYSIS (Strength and Direction)
            corr_matrix = np.corrcoef(cmd_array, meas_array)
            metrics["correlation_r"] = corr_matrix[0, 1]
            
            print(f"\n[3] CORRELATION ANALYSIS:")
            print(f" -> Pearson Correlation Coefficient (R): {metrics['correlation_r']:.4f}")
            if metrics['correlation_r'] > 0.8:
                print(" -> Interpretation: Strong positive linear tracking response.")
            else:
                print(" -> Interpretation: Weak tracking or significant mechatronic lag.")

            # 4. COMPARATIVE ANALYSIS (Comparing Two Tool Groups Across the Cleaned Slice)
            print(f"\n[4] COMPARATIVE ANALYSIS (Grouping by End-Effector Tooling):")
            groups = self.df.groupby('End_Effector_Type')['Force_Error_N'].agg(['mean', 'var', 'count'])
            metrics["comparative_groups"] = groups
            
            for tool_name, row in groups.iterrows():
                print(f" -> Tool Class [{tool_name}]: Mean Error = {row['mean']:.4f} N, Variance = {row['var']:.4f} N² (Samples: {int(row['count'])})")
            print("==================================================\n")

            return metrics
            
        except Exception as e:
            print(f" [ERROR] Technical analysis sub-routine failed: {e}")
            return {}

    def generate_static_plots(self):
        """Step 4: Renders 3 distinct required engineering static plots."""
        print("[4/5] Plotting static haptic telemetry figures...")
        try:
            if self.df is None or self.df.empty:
                return

            sns.set_theme(style="darkgrid")
            self.df['Force_Error_N'] = self.df['Measured_Force_N'] - self.df['Commanded_Force_N']

            # Plot 1: Histogram of Force Tracking Errors
            plt.figure(figsize=(6, 4))
            sns.histplot(self.df['Force_Error_N'], kde=True, color='teal')
            plt.title('End-Effector Force Error Distribution')
            plt.xlabel('Force Deviation (Newtons)')
            plt.ylabel('Frame Counts')
            plt.savefig(os.path.join(self.output_dir, "static_1_histogram.png"), bbox_inches='tight')
            plt.close()

            # Plot 2: Boxplot comparing Error distribution by tool type
            plt.figure(figsize=(6, 4))
            sns.boxplot(x='End_Effector_Type', y='Force_Error_N', data=self.df, palette='Set1')
            plt.title('Actuator Error Variances across Tool Classes')
            plt.ylabel('Force Error (N)')
            plt.savefig(os.path.join(self.output_dir, "static_2_boxplot.png"), bbox_inches='tight')
            plt.close()

            # Plot 3: Scatter Performance Plot with Regression
            plt.figure(figsize=(6, 4))
            sns.scatterplot(x='Commanded_Force_N', y='Measured_Force_N', hue='End_Effector_Type', data=self.df)
            plt.title('Haptic Force Tracking Linearity Profile')
            plt.savefig(os.path.join(self.output_dir, "static_3_scatterplot.png"), bbox_inches='tight')
            plt.close()

            print(" -> Exported 3 static quality figures to the outputs folder.")
        except Exception as e:
            print(f" [ERROR] Static visualization engine fault: {e}")

    def generate_animations(self):
        """Step 5: Renders 2 mandatory dynamic system tracking animations."""
        print("[5/5] Compiling mechatronic sequence animations...")
        try:
            if self.df is None or self.df.empty:
                return

            # Order data chronologically to show live system tracking progression
            anim_df = self.df.sort_values(by='Timestamp').reset_index(drop=True)
            frames_count = min(100, len(anim_df)) # Truncated timeline limit for rapid rendering

            # Animation 1: Dynamic Telemetry Tracking Strip
            fig, ax = plt.subplots(figsize=(6, 4))
            line_cmd, = ax.plot([], [], 'r--', label='Commanded Force', alpha=0.8)
            line_meas, = ax.plot([], [], 'b-', label='Measured Force', alpha=0.8)
            ax.set_xlim(0, frames_count)
            ax.set_ylim(anim_df['Measured_Force_N'].min() - 5, anim_df['Measured_Force_N'].max() + 5)
            ax.set_title('Live End-Effector Telemetry Strip-Chart')
            ax.set_xlabel('Haptic Query Index')
            ax.set_ylabel('Force Vector (N)')
            ax.legend()

            def init_anim1():
                line_cmd.set_data([], [])
                line_meas.set_data([], [])
                return line_cmd, line_meas

            def update_anim1(frame):
                x = np.arange(frame)
                line_cmd.set_data(x, anim_df['Commanded_Force_N'].iloc[:frame].to_numpy())
                line_meas.set_data(x, anim_df['Measured_Force_N'].iloc[:frame].to_numpy())
                return line_cmd, line_meas

            anim1 = animation.FuncAnimation(fig, update_anim1, frames=frames_count, init_func=init_anim1, blit=True)
            anim1.save(os.path.join(self.output_dir, 'animation_1_telemetry_strip.gif'), writer='pillow', fps=8)
            plt.close()

            # Animation 2: Haptic Variance Scatter Dispersion
            fig, ax = plt.subplots(figsize=(6, 4))
            ax.set_xlim(anim_df['Commanded_Force_N'].min() - 5, anim_df['Commanded_Force_N'].max() + 5)
            ax.set_ylim(anim_df['Measured_Force_N'].min() - 5, anim_df['Measured_Force_N'].max() + 5)
            ax.set_title('Dynamic Tracking Hysteresis Convergence Loop')
            ax.set_xlabel('Target Reference (N)')
            ax.set_ylabel('Actual Output Feedback (N)')
            scatter = ax.scatter([], [], c=[], cmap='coolwarm', s=30, edgecolors='none')

            def update_anim2(frame):
                x = anim_df['Commanded_Force_N'].iloc[:frame].to_numpy()
                y = anim_df['Measured_Force_N'].iloc[:frame].to_numpy()
                scatter.set_offsets(np.vstack((x, y)).T)
                scatter.set_array(y - x) # Dynamic color maps mapping current drift error
                return scatter,

            anim2 = animation.FuncAnimation(fig, update_anim2, frames=frames_count, blit=True)
            anim2.save(os.path.join(self.output_dir, 'animation_2_hysteresis_loop.gif'), writer='pillow', fps=8)
            plt.close()

            print(" -> Created and saved 2 system telemetry tracking animations.")
        except Exception as e:
            print(f" [ERROR] Animation compiler system failure: {e}")

